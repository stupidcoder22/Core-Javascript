//array push pop
//array shift unshift

let fruits = ["apple", "banana", "orange", "peech", "lemon"];
fruits.push("jackfruit");
//push add the element in array at the end
fruits.pop();
// pop removes the element who is at the last position from the array
fruits.unshift("grapes");
// it adds the element in the array at first position
fruits.shift();
// it removes the element from the first position in the array
console.log(fruits);
